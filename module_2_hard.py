def найти_пароль(n):
    """
    Находит пароль для числа n по условию кратности.

    Параметры:
    n (int): Число из первой вставки (от 3 до 20).

    Возвращает:
    str: Строка, содержащая пароль.
    """
    if n < 3 or n > 20:
        return "Число должно быть от 3 до 20."

    пароль = ""  # Переменная для хранения итогового пароля

    # Перебираем все числа от 1 до n-1
    for i in range(1, n):
        for j in range(i + 1, n):  # Пары (i, j), где j > i
            if (i + j) % n == 0:  # Проверяем кратность суммы числу n
                пароль += f"{i}{j}"  # Конкатенируем пару в строку и добавляем к паролю

    return пароль


# Пример использования:
n = int(input("Введите число (от 3 до 20): "))
print("Пароль:", найти_пароль(n))








